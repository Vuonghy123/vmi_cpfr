export const language = (function () {
    var returnValue;
    function init() {
        returnValue = 'vietnamese';
        AsyncStorage.getItem('language').then((res) => {
            returnValue = res
        })
        return returnValue
    }
    return {
        getInstance: function () {
            if (!returnValue) returnValue = init();
            return returnValue;
        },
        clearInstance: function () {
            returnValue = null
        }
    }
})();



